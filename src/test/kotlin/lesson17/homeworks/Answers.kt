   в ChildrenClass.

6. Чтобы изменить privateField из main(), добавлен публичный метод setPrivateField() 
   в BaseClass.

7. Функция getProtectedClass() не может быть публичной, потому что она возвращает 
   экземпляр ProtectedClass, который имеет модификатор protected. 
